const now = () => new Date().toISOString();
const isoAgo = (mins: number) => new Date(Date.now() - mins * 60_000).toISOString();
const rand = (min: number, max: number) => Math.round((Math.random() * (max - min) + min) * 100) / 100;

const mkJob = (i: number, name: string, fail = false): AppJob => ({
  id: `job-${i}`,
  name,
  schedule: ["*/5 * * * *", "0 * * * *", "0 0 * * *", "*/15 * * * *"][i % 4],
  lastRun: isoAgo(rand(2, 120)),
  nextRun: isoAgo(-rand(2, 60)),
  lastDurationMs: Math.round(rand(150, 8200)),
  lastStatus: fail ? "failed" : (Math.random() > 0.92 ? "failed" : "ok"),
  retries: fail ? Math.floor(rand(1, 4)) : 0,
});

const mkEndpoint = (i: number, method: AppEndpoint["method"], path: string, hot = false): AppEndpoint => ({
  id: `ep-${i}`,
  method, path,
  rpm: Math.round(rand(20, hot ? 4200 : 800)),
  errorRate: rand(0, hot ? 6.5 : 1.8),
  p95Ms: Math.round(rand(40, hot ? 1200 : 350)),
  p99Ms: Math.round(rand(80, hot ? 2200 : 700)),
  statusDist: [
    { code: 200, count: Math.round(rand(800, 5000)) },
    { code: 301, count: Math.round(rand(0, 60)) },
    { code: 400, count: Math.round(rand(0, 90)) },
    { code: 401, count: Math.round(rand(0, 30)) },
    { code: 500, count: Math.round(rand(0, hot ? 220 : 40)) },
  ],
});

const mkDb = (engine: AppDbHealth["engine"], degraded = false): AppDbHealth => ({
  name: `${engine}-cluster`,
  engine,
  uptimeDays: Math.floor(rand(8, 740)),
  connections: Math.round(rand(20, degraded ? 980 : 400)),
  maxConnections: 1000,
  slowQueries: Math.round(rand(0, degraded ? 240 : 12)),
  locks: Math.round(rand(0, degraded ? 18 : 2)),
  replicationLagMs: Math.round(rand(0, degraded ? 12000 : 250)),
  storageUsedPct: rand(20, degraded ? 92 : 68),
});

const mkLog = (i: number, level: AppLog["level"], source: string, message: string): AppLog => ({
  ts: isoAgo(rand(0, 240)),
  level, source, message,
});

